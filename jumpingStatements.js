// break
for(let i = 1; i <= 10; i++) {
    if (i == 6) {
        break; // stop at 6 the exit from the loop
    }
    console.log(i);
}

// continue
for(let x = 1; x <= 10; x++) {
    if (x == 3) {
        continue; // 3 is skipped the continue if statement still meets the condition
    }
    console.log(x);
}