let weekNo = 11;

if(weekNo == 1) {
    console.log("Sunday.");
} else if(weekNo == 2) {
    console.log("Monday.");
} else if(weekNo == 3) {
    console.log("Tuesday.");
} else if(weekNo == 4) {
    console.log("Wednesday.");
} else if(weekNo == 5) {
    console.log("Thursday.");
} else if(weekNo == 6) {
    console.log("Friday.");
} else if(weekNo == 7) {
    console.log("Saturday.");
} else {
    console.log("Invalid week number.");
}